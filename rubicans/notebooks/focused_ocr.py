class ViTOutput(nn.Module):
    def __init__(self) -> None:
        """
        This is actually the resnet block 
        """
        super().__init__()
        self.dense = nn.Linear(3072, 768)
        self.dropout = nn.Dropout(0., inplace=True)

    def forward(self, hidden_states: torch.Tensor, input_tensor: torch.Tensor) -> torch.Tensor:
        hidden_states = self.dense(hidden_states)
        hidden_states = self.dropout(hidden_states)
        
        # residual connection
        hidden_states = hidden_states + input_tensor

        return hidden_states


class ViTLayer(nn.Module):
    """This corresponds to the Block class in the timm implementation."""

    def __init__(self, config: ViTConfig) -> None:
        super().__init__()
        self.chunk_size_feed_forward = config.chunk_size_feed_forward
        self.seq_len_dim = 1
        self.attention = FocusedLinearAttention()
        self.intermediate = ViTIntermediate()
        self.output = ViTOutput()
        self.layernorm_before = nn.LayerNorm(768, eps=1e-12)
        self.layernorm_after = nn.LayerNorm(768, eps=1e-12)

    def forward(
        self,
        hidden_states: torch.Tensor,
        head_mask: Optional[torch.Tensor] = None,
        output_attentions: bool = False,
    ) -> Union[Tuple[torch.Tensor, torch.Tensor], Tuple[torch.Tensor]]:
        self_attention_outputs = self.attention(
            self.layernorm_before(hidden_states)  # in ViT, layernorm is applied before self-attention
        )
        attention_output = self_attention_outputs[0]
        # outputs = self_attention_outputs[1:]  # add self attentions if we output attention weights

        # first residual connection
        hidden_states = attention_output + hidden_states

        # in ViT, layernorm is also applied after self-attention
        layer_output = self.layernorm_after(hidden_states)
        layer_output = self.intermediate(layer_output)

        # second residual connection is done here
        layer_output = self.output(layer_output, hidden_states)

        outputs = (layer_output,) + outputs

        return outputs


